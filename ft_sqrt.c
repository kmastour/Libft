int		ft_sqrt(int nb)
{
	int		i;
	int		resultat;

	i = 0;
	resultat = 0;
	while (i < nb && resultat == 0)
	{
		if (i * i == nb)
			resultat = i;
		i++;
	}
	return (resultat);
}
